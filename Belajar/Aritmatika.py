a = 10
b = 2

#penjumblahan
hasil = a + b
print(a,'+',b,'=',hasil)

#penguraangan
hasil = a - b
print(a,'-',b,'=',hasil)

#pembagian
hasil = a / b
print(a,'/',b,'=',hasil)

#perkalian
hasil = a * b
print(a,'*',b,'=',hasil)

#pangkat
hasil = a ** b
print(a,'**',b,'=',hasil)
